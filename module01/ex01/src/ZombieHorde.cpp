#include "Zombie.hpp"

/* ************************************************************************** */
/* ------------------------------ CONSTRUCTOR ------------------------------- */
/* ************************************************************************** */

Zombie* zombieHorde( int N, std::string name )
{
	int i = -1;
	Zombie *Horde[N];
	
	while (++i < N)
	{
		Horde[i] = newZombie(name); 
		Horde[i]->announce();
	}
	i = -1;
	while (++i < N)
	{
		delete(Horde[i]); 
	}
	return (Horde[0]);
}
