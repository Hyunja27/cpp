#include "Zombie.hpp"

/* ************************************************************************** */
/* ------------------------------ CONSTRUCTOR ------------------------------- */
/* ************************************************************************** */

Zombie* zombieHorde( int N, std::string name )
{
	int i = -1;
	Zombie *Horde = new Zombie[N];

	while (++i < N)
	{
		Horde[i].name = name; 
		Horde[i].announce();
	}

	return (Horde);
}
